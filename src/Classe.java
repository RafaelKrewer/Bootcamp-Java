public class Classe {
  public static void main(String[] args) {

    int numero1 = 35;
    int numero2 = 36;

    int somar = numero1 + numero2;
    int diminuir = numero1 - numero2;
    System.out.println(numero1 + " + " + numero2 + " = " + somar);
    System.out.println(numero1 + " - " + numero2 + " = " + diminuir);
  }
}